    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String u= jTextField1.getText();
        String p= jTextField2.getText();
        boolean check = true;
        
        if(check&u.isEmpty()){
            check= false;
            JOptionPane.showMessageDialog(this,"Mời nhập tài khoản ...");
        }
        if(check&p.isEmpty()){
            check=false;
            JOptionPane.showMessageDialog(this,"Mời nhập mật khẩu ...");
        }
        if(check){
            if(dll.getLogin(u, p)){
                this.setVisible(false);
                Home m=new Home();
                m.SsayHi.setText(u);
                m.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(this,"Login fail");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

